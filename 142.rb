# QUESTION 1
# Write a program that starts with an array of strings, 
# then prints out each string on separate lines using a while loop.

fruit = ["apple", "orange", "banana", "grape", "watermelon"]
index = 0
while index < fruit.length
    puts fruit[index]
    index = index + 1
end

